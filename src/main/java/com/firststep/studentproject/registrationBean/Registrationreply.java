package com.firststep.studentproject.registrationBean;
public class Registrationreply {
private	int userId;
private String	userFirstName;
private String	userSurname;
private int	userPinCode;
private int	userJoinDate;
private int	userDob;
public int getUserId() {
	return userId;
}
public void setUserId(int userId) {
	this.userId = userId;
}
public String getUserFirstName() {
	return userFirstName;
}
public void setUserFirstName(String userFirstName) {
	this.userFirstName = userFirstName;
}
public String getUserSurname() {
	return userSurname;
}
public void setUserSurname(String userSurname) {
	this.userSurname = userSurname;
}
public int getUserPinCode() {
	return userPinCode;
}
public void setUserPinCode(int userPinCode) {
	this.userPinCode = userPinCode;
}
public int getUserJoinDate() {
	return userJoinDate;
}
public void setUserJoinDate(int userJoinDate) {
	this.userJoinDate = userJoinDate;
}
public int getUserDob() {
	return userDob;
}
public void setUserDob(int userDob) {
	this.userDob = userDob;
}


}
